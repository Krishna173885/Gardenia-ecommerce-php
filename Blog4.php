<h2>What is Hydroponics?</h2>
             
<p2 >
The thought of gardening is sure to have you all agog and raring to go. However, many people, especially city dwellers are reluctant to get their hands dirty and take the pain to clear up the mess afterward. Yes! Indoor gardens can be better managed but again, you would have to handle wet mud and take care to trim the plants. Moreover, the scarcity of soil is a problem that is likely to trouble you. Why not opt for growing plants without soil? Yes, you can grow garden plants without using a particle of soil. Sounds unbelievable, doesn’t it? Well, this article tells you all you need to know about growing plants at home and without soil.You may think that you need soil, sunlight, and water to grow plants. Well, not anymore. Now you can do away with soil for growing plants. This method of growing plants without soil is called hydroponics.
<br>
Well, it is a scientifically proven procedure of nourishing plants with nutrients mixed in water. The soil for growing plants is not the medium to supply nutrients. The gardener has to replenish the water of the garden plants regularly by adding nutrients.
                   </p2>
                      
                   <br>
                  
<h2>Water quality</h2>                
<p2>
It is important to use the purest water possible. Remember that there is no soil that you can use when trying to grow hydroponic plants. Admittedly you would use filtered water, but it is a good idea to have the sample checked at the laboratory to confirm its purity. Being lackadaisical about this aspect can affect the entire procedure as municipality water is known to contain a variety of toxins along with harsh chemicals that harm garden plants causing them to wilt and die before long.</p2>

<h2>Balancing of Nutrients</h2>
<p2>
It is advisable to check the manufacturer’s recommendation and use the nutrients in the right dosage. Going overboard with nutrients is a strict no-no as more is not necessarily beneficial. Feel free to mix the stipulated amount of nutrients into the water reservoir that serves as the medium for growth. You may use a TDC EC meter to measure the electric current transported via water so that the right quantity of nutrients is transported to the growing plants. Be sure to use every nutrient as per the specifications. Overfeeding plants without soil may be counterproductive.</p2>
                      
<h2>Optimum pH</h2>
<p2>
Both EC and pH are essential to grow for plants in the soil, and you must keep a close eye on the pH to strike the right balance. Well, you may have to do it for the soil-growing plants too, but the need is much reduced as the soil takes care to transport the nutrients to the roots. You have to keep a check on the pH of the growing medium and keep it at an optimal level. You may reduce the pH too when growing plants without soil. Do this by adding a few drops of a mild acid to the water. However, be careful to keep the right pH balance for hydroponic plants.
</p2> 

<h2>Oxygen</h2>
<p2>
Photosynthesis ensures the supply of oxygen to plants growing naturally in the soil. You have to add oxygen from the outside when trying to grow hydroponic plants. If you are growing plants without soil, use air pumps, air bubblers, air stones, or an NFT (Nutrient Filming Technique) structure. It is equally important to maintain the ideal temperature of the water. Remember to maintain it at 18-22 °C to ensure that the right quantity of oxygen reaches the root system.All of the above-mentioned factors together with the right environment can go a long way in ensuring the steady growth of hydroponic plants. You will not feel the need to source indoor soil for plants when you can grow greens perfectly at home.


</p2> 

<h2>Conclusion</h2>                     
<p2>
Space is not a problem when you have the will. The way forward is to embrace hydroponics which allows you to grow plants without soil in an alternative environment without the usage of soil at all. Follow the tips diligently and consider all necessary factors to ensure a thriving garden. Pluck healthy, nutritious greens to enhance your meals. You may create a beautiful flower garden without using any soil for growing plants as well. </p2>
   
