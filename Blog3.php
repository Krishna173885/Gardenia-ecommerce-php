<h2>Benefits of dividing congested pot-plants:</h2>
             
<p2 >
Once a plant has captured the entire pot, and if it is not practical to move it to a larger one, the division may revive it and will also give you a number of extra plants. Not all plants respond to the division. Those with a fibrous root system, like calatheas and most ferns, are more likely to divide successfully.A few flowering plants bloom better if kept slightly pot-bound. Check if a particular plant responds well to division in an encyclopedia.
House and greenhouse plants can be divided throughout the year, but late spring and early summer are particularly good times.<br>
Benefits:
<br>
*Create “new” plants
<br>
*Rejuvenate old, overcrowded plants
<br>
*Regrow expensive plant  varieties without spending 
                   </p2>
                      
                   <br>
                  
<h2>Plants that grow after division:</h2>                
<p2>
Perennials, grasses, bulbs, or suckering shrubs would grow after they are divided.</p2>

<h2>Plants that won’t grow after division:</h2>
<p2>
Poppy, False Indigo, Butterfly Weed, Lupine, Peony, and Sea Holly to name a few.
Avoid dividing plants with a taproot, i.e. downward-pointing root resembling a carrot.
<br>
Things you will need to divide congested pot-plants:
<br>
Knife,
Pots,
Potting Soil.</p2>
                      
<h2>How to divide congested pot-plants?</h2>
<p2>
Step 1: Water the plant at least half an hour before you divide it. If the plant does not come out of the pot easily, try inverting the container while you support the plant and tap its rim on a hard surface.<br>
Step 2: Remove any broken pots used to cover the old drainage hole in a clay pot, and pull away a little potting soil to expose some of the roots and make division easier.<br>
Step 3: Most plants can be pulled apart by hand, but if the root-ball is difficult to break open this way use a hand fork.<br>
Step 4: Break the clump into smaller pieces. If you want many plants, divide into quite small pieces, but if you need only one or two, and prefer to start again with larger plants, two or three pieces may be appropriate. It may be necessary to trim off a few of the largest roots to fit the plant in its new pot.<br>
Step 5: Replant the sections as soon as possible, trickling potting soil around the roots. If possible, use the same kind of potting material as the plant was in before, and firm it well around the roots. Water and keep out of direct sunlight for a few days.
<br>
</p2> 
                     
<p2>
Tip: Provide regular watering to your newly potted plants. Do not feed them with fertilizer at least for a month or two, or until they are firmly rooted and self-sufficient. </p2>
   
